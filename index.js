// ******************Jackets***************
function oneJacket(x) {
    return [
        '<p class="product-description"><img src="' +
            x.jackets.image +
            '"/>&emsp;',
        '<br><br><p class="product-description">Price: $' +
            x.jackets.price +
            '</p>',
        '<p class="product-description">In Stock: ' + x.jackets.stock + '</p>',
        '<p class="product-description">Type: ' +
            x.jackets.description +
            '</p>',
        '<span class="product-description">Add to cart  <i onclick="addToCart" class="fa fa-plus-square" aria-hidden="true"></i></span><br><br>'
    ].join('');
}

function showJackets() {
    var h = CLOTHING.outerwear
        .map(function(x) {
            return oneJacket(x);
        })
        .join('');
    var html =
        [
            '<h4><span id="open-sidebar2" onclick="openNav()">&#9776;More Clothes</span></h4><h2 class="shopping-header"><img class="header-company-logo" src="photos/company-logo.png">Elegant Spirit</h2><hr />'
        ].join('') + h;
    $('#jackets').html(html);
}

